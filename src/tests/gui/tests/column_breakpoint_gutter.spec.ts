/**
 * M6 — Column-Aware Replay Navigation: GUI gutter column-click affordance.
 *
 * Spec:
 *   codetracer-specs/Planned-Features/Column-Aware-Navigation.status.org
 *   §M6 Acceptance tests — GUI Playwright.
 *
 * What this spec proves:
 *
 *   1. Alt+clicking on a line's text at a column > 1 invokes the
 *      M1 `addColumnBreakpoint(path, line, column)` service path —
 *      the breakpoint stored in
 *      `data.services.debugger.breakpointTable[path][line]` carries
 *      the exact column Monaco resolved from the click x-offset.
 *   2. After Alt+click + Continue the replay engine halts at the
 *      bound column — end-to-end proof that the M6 click→column
 *      glue flows into the M1 wire path.
 *   3. Clicking the CodeTracer gutter HTML (`.gutter-line`) at the
 *      far-left edge keeps the legacy line-only behaviour — column
 *      = 0 — preserving the M1 back-compat invariant.
 *   4. A column-anchored Monaco decoration is rendered on the
 *      target line — verifies the M6 "marker visibly anchors to the
 *      column" deliverable.  The decoration carries a hover message
 *      naming the bound column for the M6 tooltip deliverable.
 *
 * Fixture: reuses the M1 column-aware multi-statement JS recording
 * generated by `prepareFixture` from `column_breakpoint.spec.ts`.
 */
