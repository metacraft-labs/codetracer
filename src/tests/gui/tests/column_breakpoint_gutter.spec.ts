test.describe("M6 — Column-aware gutter / text click affordance", () => {
  test("alt_click_on_line_text_registers_column_aware_breakpoint", async ({
    ctPage,
  }) => {
    await readyOnEntry(ctPage);
    const layout = new LayoutPage(ctPage);
    await layout.runToEntryButton().click();
    await expect(ctPage.locator(".location-path")).not.toHaveText(":0#0", {
      timeout: 30_000,
    });
    const editors = await layout.editorTabs(true);
    const editor = editors.find((e) => e.fileName === "program.js");
    expect(editor, "program.js editor tab should be open").toBeDefined();
    if (!editor) return;

    const target = await resolveColumnClickTarget(
      editor,
      1,
      fixture.laterStatementColumn,
    );
    // Alt+click — the M6 affordance.  No modifier-free click here:
    // a plain click on the text positions the cursor, it does not
    // toggle a breakpoint.  We use `altMouseDownAt` rather than
    // `page.mouse.click(..., { modifiers: ["Alt"] })` because the
    // latter's CDP-synthesized pointer event under Xvfb does not
    // reach Monaco's `mousedown` listener with the `altKey`
    // modifier preserved (see helper docs above).
    await altMouseDownAt(editor, target.x, target.y);

    // The breakpoint MUST be registered with the exact column Monaco
    // resolved at the click x.
    await expect
      .poll(async () => (await readBreakpoint(editor, 1))?.column ?? -1)
      .toBe(fixture.laterStatementColumn);
    const bp = await readBreakpoint(editor, 1);
    expect(bp, "column-aware breakpoint must be registered").not.toBeNull();
    expect(bp!.enabled).toBe(true);

    // The column-anchored Monaco decoration MUST be present at the
    // bound column.  Validates the M6 "marker anchors to column"
    // deliverable.
    await expect
      .poll(async () => (await getColumnMarkerDecorations(editor, 1)).length)
      .toBeGreaterThan(0);
    const decorations = await getColumnMarkerDecorations(editor, 1);
    const found = decorations.find(
      (d) => d.startColumn === fixture.laterStatementColumn,
    );
    expect(
      found,
      `decoration at startColumn=${fixture.laterStatementColumn} not found`,
    ).toBeDefined();
    expect(found!.endColumn).toBeGreaterThan(found!.startColumn);
    // Hover tooltip must name the bound column (M6 tooltip
    // deliverable).
    expect(found!.hoverText ?? "").toContain(
      `column ${fixture.laterStatementColumn}`,
    );

    // End-to-end: Continue must halt at the bound (line, column).
    await layout.continueButton().click();
    await expect.poll(async () => await getCurrentLine(editor)).toBe(1);
    await expect
      .poll(async () => await getCurrentColumn(editor))
      .toBe(fixture.laterStatementColumn);
  });

  test("gutter_click_at_line_start_falls_through_to_line_only", async ({
    ctPage,
  }) => {
    await readyOnEntry(ctPage);
    const layout = new LayoutPage(ctPage);
    await layout.runToEntryButton().click();
    await expect(ctPage.locator(".location-path")).not.toHaveText(":0#0", {
      timeout: 30_000,
    });
    const editors = await layout.editorTabs(true);
    const editor = editors.find((e) => e.fileName === "program.js");
    expect(editor, "program.js editor tab should be open").toBeDefined();
    if (!editor) return;

    // Click the gutter HTML on line 2 — the canonical legacy path
    // verified by the M1 spec.  The breakpoint MUST carry column = 0
    // (the legacy sentinel) so the M1 back-compat invariant holds
    // end-to-end.
    await editor.gutterElement(fixture.legacyLine).click();
    await expect
      .poll(() => editor.hasBreakpointAt(fixture.legacyLine))
      .toBeTruthy();

    const bp = await readBreakpoint(editor, fixture.legacyLine);
    expect(bp, "legacy line-only breakpoint must be registered").not.toBeNull();
    expect(bp!.column).toBe(0);

    // The gutter click MUST NOT emit a column-anchored decoration on
    // the legacy-line — that would lie about the (absent) bound
    // column.
    const decorations = await getColumnMarkerDecorations(
      editor,
      fixture.legacyLine,
    );
    expect(decorations.length).toBe(0);

    // Continue must halt on the legacy line (line-only matching).
    await layout.continueButton().click();
    await expect
      .poll(async () => await getCurrentLine(editor))
      .toBe(fixture.legacyLine);
  });
});
