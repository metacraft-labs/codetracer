interface BreakpointSnapshot {
  line: number;
  column: number;
  enabled: boolean;
  path: string;
}

/** Read the breakpoint at `(path, line)` from the frontend's
 *  `breakpointTable`.  Mirrors the M1 spec helper of the same name. */
async function readBreakpoint(
  editor: EditorPane,
  line: number,
): Promise<BreakpointSnapshot | null> {
  return editor.page.evaluate(
    ({ path: p, line: l }) => {
      const w = window as any; // eslint-disable-line @typescript-eslint/no-explicit-any
      const table = w?.data?.services?.debugger?.breakpointTable;
      if (!table) return null;
      const aliases = [p];
      if (p.startsWith("/private/var/")) aliases.push(p.slice("/private".length));
      else if (p.startsWith("/var/")) aliases.push("/private" + p);
      for (const alias of aliases) {
        const bp = table[alias]?.[l];
        if (bp) {
          return {
            line: bp.line,
            column: bp.column ?? 0,
            enabled: !!bp.enabled,
            path: alias,
          };
        }
      }
      return null;
    },
    { path: editor.filePath, line },
  );
}

async function getCurrentColumn(editor: EditorPane): Promise<number | null> {
  return editor.page.evaluate(() => {
    const w = window as any; // eslint-disable-line @typescript-eslint/no-explicit-any
    const loc = w?.data?.services?.debugger?.location;
    if (!loc) return null;
    if (typeof loc.column !== "number") return null;
    return loc.column;
  });
}

async function getCurrentLine(editor: EditorPane): Promise<number | null> {
  return editor.page.evaluate(() => {
    const w = window as any; // eslint-disable-line @typescript-eslint/no-explicit-any
    const loc = w?.data?.services?.debugger?.location;
    if (!loc) return null;
    if (typeof loc.line !== "number") return null;
    return loc.line;
  });
}

interface ClickTarget {
  x: number;
  y: number;
}

/**
 * Resolve the on-screen pixel position of `(line, column)` inside the
 * editor's text view, using Monaco's `getOffsetForColumn` to map
 * column → pixel x and the gutter row's bounding rect for pixel y.
 *
 * Returns absolute viewport coordinates suitable for
 * `page.mouse.click(x, y, { modifiers: ["Alt"] })`.  The M6 click
 * handler reads the Monaco-resolved column from
 * `e.target.position.column`, which is what Monaco computes from the
 * exact click x — so as long as we land somewhere inside the column N
 * glyph box, Monaco reports column N.
 */
async function resolveColumnClickTarget(
  editor: EditorPane,
  line: number,
  column: number,
): Promise<ClickTarget> {
  const target = await editor.root.evaluate(
    (paneRoot: Element, { line, column }) => {
      const editorEl = paneRoot.querySelector(".monaco-editor");
      if (!editorEl) return null;
      const w = window as any; // eslint-disable-line @typescript-eslint/no-explicit-any
      const data = w?.data;
      const monacoEditor = data?.ui?.editors?.[data?.services?.editor?.active]
        ?.monacoEditor;
      if (!monacoEditor) return null;
      const gutter = editorEl.querySelector(
        `.margin-view-overlays .gutter[data-line='${line}']`,
      );
      const viewLinesEl = editorEl.querySelector(".view-lines");
      if (!gutter || !viewLinesEl) return null;
      const gRect = gutter.getBoundingClientRect();
      const vRect = viewLinesEl.getBoundingClientRect();
      if (gRect.height === 0 || vRect.width === 0) return null;
      // `getOffsetForColumn(line, column)` returns the pixel x of the
      // column relative to the editor's content area (after the
      // gutter).  Add `vRect.left` to get an absolute viewport x.
      const offsetX = monacoEditor.getOffsetForColumn(line, column);
      // Click in the middle of the column N glyph box rather than at
      // its left edge — Monaco resolves a click on the exact edge to
      // column N or N-1 ambiguously depending on sub-pixel rounding.
      //
      // We compute the glyph half-width from Monaco itself by
      // asking for the offset of column N+1; the difference is the
      // exact rendered glyph advance.  Using the line height as an
      // approximation overshoots for narrow monospace fonts and
      // pushes the click into column N+1.
      const nextOffsetX = monacoEditor.getOffsetForColumn(line, column + 1);
      const glyphWidth =
        nextOffsetX > offsetX ? nextOffsetX - offsetX : 6;
      const halfGlyph = glyphWidth / 2;
      return {
        x: vRect.left + offsetX + halfGlyph,
        y: gRect.top + gRect.height / 2,
      };
    },
    { line, column },
  );
  if (!target) {
    throw new Error(
      `Could not resolve click target for (line=${line}, column=${column})`,
    );
  }
  return target;
}

/**
 * Dispatch an Alt+mousedown directly on the Monaco element that sits
 * under viewport coordinates `(x, y)`.
 *
 * Why not `page.mouse.click(..., { modifiers: ["Alt"] })`?  Monaco's
 * `MouseHandler` subscribes to `mousedown` (DOM event) on the editor's
 * view dom node and constructs its `IEditorMouseEvent` from the DOM
 * event's target + clientX/clientY.  Under Xvfb + Playwright's CDP
 * mouse-click path, the synthesized OS-level pointer event lands on
 * the document but does NOT always propagate the `altKey` modifier
 * through to Monaco's mousedown listener (observed: the
 * `onMouseDown` handler simply doesn't fire — verified by the
 * absence of any "M6 onMouseDown fired" cdebug log lines in the
 * test diagnostic dump).
 *
 * Dispatching a real `MouseEvent` with `altKey: true` on the element
 * resolved via `elementFromPoint(x, y)` puts the event directly into
 * Monaco's listener chain, so the M6 onMouseDown → lineActionClickAt
 * → setColumnBreakpoint chain executes end-to-end — exactly what
 * the M6 acceptance test must exercise.
 *
 * This does NOT bypass M6's dispatch logic: the event is processed
 * by Monaco's own `MouseHandler`, which constructs the IMouseTarget
 * (with `position.lineNumber` / `position.column` derived from the
 * click x) and fires `monacoEditor.onMouseDown`, where the M6
 * Alt-branch lives.  We are only changing *how* the click event
 * reaches Monaco, not what Monaco does with it.
 */
async function altMouseDownAt(
  editor: EditorPane,
  x: number,
  y: number,
): Promise<void> {
  const dispatched = await editor.page.evaluate(
    ({ x, y }) => {
      // Resolve the topmost element at the viewport coordinate so
      // Monaco's `MouseTargetFactory` receives an event whose
      // `target` it can map back to a `(line, column)` position.
      const el = document.elementFromPoint(x, y);
      if (!el) return false;
      const dispatchOn = (target: Element, type: string): void => {
        const evt = new MouseEvent(type, {
          bubbles: true,
          cancelable: true,
          view: window,
          button: 0,
          buttons: type === "mousedown" ? 1 : 0,
          clientX: x,
          clientY: y,
          screenX: x,
          screenY: y,
          altKey: true,
          ctrlKey: false,
          shiftKey: false,
          metaKey: false,
        });
        target.dispatchEvent(evt);
      };
      // Monaco's MouseHandler listens for `mousedown` on the editor
      // view DOM node (via event bubbling).  Sending a plain
      // `mousedown` is enough to drive `onMouseDown`; we follow
      // with a `mouseup` to keep DOM state coherent for any
      // subsequent assertions.
      dispatchOn(el, "mousedown");
      dispatchOn(el, "mouseup");
      return true;
    },
    { x, y },
  );
  if (!dispatched) {
    throw new Error(
      `altMouseDownAt: no element under viewport coordinate (${x}, ${y})`,
    );
  }
}

async function getColumnMarkerDecorations(
  editor: EditorPane,
  line: number,
): Promise<{ startColumn: number; endColumn: number; hoverText: string | null }[]> {
  return editor.page.evaluate(
    ({ line }) => {
      const w = window as any; // eslint-disable-line @typescript-eslint/no-explicit-any
      const data = w?.data;
      const editorComp = data?.ui?.editors?.[data?.services?.editor?.active];
      const monacoEditor = editorComp?.monacoEditor;
      if (!monacoEditor) return [];
      const model = monacoEditor.getModel?.();
      if (!model) return [];
      const decos = model.getLineDecorations
        ? model.getLineDecorations(line)
        : [];
      const out: {
        startColumn: number;
        endColumn: number;
        hoverText: string | null;
      }[] = [];
      for (const d of decos) {
        const cls = d?.options?.inlineClassName;
        if (cls !== "ct-column-breakpoint-marker") continue;
        const hover = d?.options?.hoverMessage;
        let hoverText: string | null = null;
        if (hover) {
          if (typeof hover === "string") hoverText = hover;
          else if (typeof hover?.value === "string") hoverText = hover.value;
        }
        out.push({
          startColumn: d?.range?.startColumn ?? 0,
          endColumn: d?.range?.endColumn ?? 0,
          hoverText,
        });
      }
      return out;
    },
    { line },
  );
}

