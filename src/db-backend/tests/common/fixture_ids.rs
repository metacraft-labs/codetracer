#[cfg(test)]
mod tests {
    use super::*;

    /// Length-and-hyphen-position check: every constant is a canonical
    /// 36-character UUIDv7 string. This guards against typos when adding
    /// new constants — a missing or extra hex digit (or a stray uppercase
    /// letter) would shift the hyphens and trip this test.
    #[test]
    fn every_constant_is_canonical_uuid_v7() {
        for id in ALL_FIXTURE_RECORDING_IDS {
            assert_eq!(id.len(), 36, "{id} is not 36 chars long");

            // Hyphens at positions 8, 13, 18, 23.
            for &pos in &[8usize, 13, 18, 23] {
                assert_eq!(id.as_bytes()[pos], b'-', "{id}: position {pos} is not '-'");
            }

            // Version nibble (position 14) must be '7'.
            assert_eq!(
                id.as_bytes()[14],
                b'7',
                "{id}: version nibble at position 14 is not '7'"
            );

            // Variant nibble (position 19) must be one of {8, 9, a, b}.
            let variant = id.as_bytes()[19];
            assert!(
                matches!(variant, b'8' | b'9' | b'a' | b'b'),
                "{id}: variant nibble at position 19 must be 8/9/a/b, was 0x{variant:02x}"
            );

            // All non-hyphen characters must be lowercase hex.
            for (i, ch) in id.char_indices() {
                if matches!(i, 8 | 13 | 18 | 23) {
                    continue;
                }
                assert!(
                    matches!(ch, '0'..='9' | 'a'..='f'),
                    "{id}: character {ch:?} at position {i} is not lowercase hex"
                );
            }
        }
    }

    /// No two fixtures share an id — collisions would conflict in
    /// `trace_index.db` which uses `recording_id` as the primary key.
    #[test]
    fn all_fixture_ids_are_unique() {
        let mut seen = std::collections::HashSet::new();
        for id in ALL_FIXTURE_RECORDING_IDS {
            assert!(seen.insert(*id), "fixture id {id} is listed twice");
        }
    }
}
