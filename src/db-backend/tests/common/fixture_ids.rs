//! Canonical UUIDv7 `recording_id`s for the committed example recordings.
//!
//! Mirror of
//! [`codetracer-example-recordings/FIXTURE_IDS.md`](../../../../codetracer-example-recordings/FIXTURE_IDS.md)
//! and the Nim sibling
//! [`codetracer/src/common/fixture_ids.nim`](../../../common/fixture_ids.nim).
//!
//! **Authority:** the Markdown file. If you change a fixture's id, change
//! it in all three places. The Rust side is `const &str` so misspellings
//! are caught at compile time when tests reference them.
//!
//! ## Why a module instead of inline literals
//!
//! Two reasons:
//!
//! 1. **Single point of update.** A new test that wants to assert
//!    "this is the python flow_test recording" imports
//!    `PYTHON_FLOW_TEST_RECORDING_ID` rather than re-typing the UUID.
//!    If we ever need to rotate an id (e.g. because the embedded
//!    timestamp leaked into a user-visible URL and we want a tidier
//!    one) we change exactly one constant.
//!
//! 2. **Documentation co-location.** Each constant carries a one-line
//!    comment naming the fixture path it pins, so `grep` on a UUID
//!    string from a failing test goes straight to a human-readable
//!    description.
//!
//! ## Why `pub const &str` and not `Uuid`
//!
//! db-backend's wire types carry `recording_id: String` (M-REC-4). The
//! crate does not (yet) introduce a typed wrapper, so the constants are
//! exposed as `&'static str` to match the field type at the use site
//! without `.to_string()` ceremony.
//!
//! Generated by M-REC-12 (Recording-Identifier Migration, fixture
//! freeze). See
//! [`codetracer-specs/Refactoring-Plans/Recording-Identifier-Migration.md`](https://github.com/metacraft-labs/codetracer-specs)
//! §6.10.

