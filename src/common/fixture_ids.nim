# ─── Compile-time sanity checks ─────────────────────────────────────────

static:
  ## Every constant in this module must be a canonical UUIDv7 string.
  ##
  ## ``isCanonicalUuidV7`` (from ``recording_id``) enforces:
  ##   - length == 36
  ##   - hyphens at positions 8/13/18/23
  ##   - version nibble '7' at position 14
  ##   - variant nibble in {8,9,a,b} at position 19
  ##   - all other characters are lowercase hex
  ##
  ## Catching typos at compile time means a malformed id never reaches a
  ## test run.
  for id in allFixtureRecordingIds:
    doAssert isCanonicalUuidV7(id),
      "fixture_ids.nim: '" & id & "' is not a canonical UUIDv7 — " &
      "check hyphen positions, version nibble, variant nibble, and case."

  ## No duplicate ids.  ``recording_id`` is the primary key of the local
  ## ``recordings`` table (M-REC-2 §5), so two fixtures sharing an id
  ## would conflict if both were ever indexed by the same database.
  for i in 0 ..< allFixtureRecordingIds.len:
    for j in (i + 1) ..< allFixtureRecordingIds.len:
      doAssert allFixtureRecordingIds[i] != allFixtureRecordingIds[j],
        "fixture_ids.nim: '" & allFixtureRecordingIds[i] &
        "' is listed twice (positions " & $i & " and " & $j & ")."
