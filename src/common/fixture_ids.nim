## Canonical UUIDv7 ``recording_id`` constants for the committed example
## recordings.
##
## Mirror of
## ``codetracer-example-recordings/FIXTURE_IDS.md`` and the Rust sibling
## ``codetracer/src/db-backend/tests/common/fixture_ids.rs``.  Keep all
## three in lockstep — the Markdown file is the authority.
##
## # Why a separate module
##
## Tests that previously hard-coded ``traceId = 1`` (or similar
## integer ids) now reference these constants directly.  This avoids:
##
## - Re-typing 36-character UUIDs in every test (which is both error-prone
##   and hard to grep — a misspelled UUID looks identical to the right
##   one until the comparison fails).
## - Coupling tests to id-generation behaviour.  Tests that assert
##   "the python flow_test recording loads" compare against
##   ``PYTHON_FLOW_TEST_RECORDING_ID`` rather than asserting on the
##   process of minting ids.
##
## # Source of truth
##
## Generated by **M-REC-12** of the Recording-Identifier Migration; see
## ``codetracer-specs/Refactoring-Plans/Recording-Identifier-Migration.md``
## §6.10 and §8.  The embedded UUIDv7 timestamp is fictional (pinned to
## ``2026-05-18T08:30:22Z``) — the honest creation date of each fixture
## lives in the example-recordings git history.

